        function [X,Y,Wx,Wy] = triQuad(this,n)
            N = this.ng;
            if nargin > 1
                N = n;
            end
            v = [0 0; 0 1; 1 0];
            n=1:N;  nnk=2*n+1; A=[1/3 repmat(1,1,N)./(nnk.*(nnk+2))];
            n=2:N; nnk=nnk(n); B1=2/9; nk=n+1; nnk2=nnk.*nnk;
            B=4*(n.*nk).^2./(nnk2.*nnk2-nnk2); ab=[A' [2; B1; B']]; s=sqrt(ab(2:N,2));
