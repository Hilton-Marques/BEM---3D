        function this = Solver(ng,n,nNodes)
            this.ng = ng;
            this.n = n;
            %max capacity of moments, remeber that the terms forms a triangle,
            %so the total numbers of terms is Sn = (a + an)n/2
            %Besides that, I also have n + 1 terms
            a1 = 1;
            an = 2*(n)+1;
            this.totN = (a1 + an)*(n+1)/2;
            this.Ax = zeros(nNodes,1);
        end
        function calculateME(this,element)
            element.MEH = zeros(this.totN,1);
            element.MEG = zeros(this.totN,1);
            [X,Y,Wx,Wy] = this.triQuad;
            yc = element.getYc;
            fieldPoints = element.getFieldPoints();
            for i = 1:length(fieldPoints)
                field = fieldPoints(i);
                if (field.q(1) == 1)
                    u = field.q(2);  %temperature
                    count = 1;
                    elementPoints = element.getElementPoints();
                    for n = 0:this.n
                        for m = -n:n
                            for o = 1:this.ng
                                for p = 1:this.ng
                                    xksi = zeros(3,1);
                                    shapeF = element.getShapeF(X(p,o),Y(p,o));
                                    for s = 1:length(elementPoints)
                                        xksi = xksi + shapeF(s)*points(s).coord;
                                    end
                                    r = xksi - yc;
                                    jacobian = element.getJacobian(X(p,o),Y(p,o));
                                    normal = element.getNormal(X(p,o),Y(p,o));
                                    dR = [-0.5*( this.RLook(r,this.indexNM(n-1,m-1),n-1) - ...
                                        this.RLook(r,this.indexNM(n-1,m+1),n-1) ) , ...
                                        -(complex(0,1)/2) * (this.RLook(r,this.indexNM(n-1,m-1),n-1) +...
                                        this.RLook(r,this.indexNM(n-1,m+1),n-1) ), ...
                                        this.RLook(r,this.indexNM(n-1,m),n-1) ];
                                    element.MEH(count) = element.MEH(count) + ...
                                        (jacobian/0.5)*u*(dR*normal)*Wx(p)*Wy(o)*shapeF(i);
                                end
                            end
                            count = count + 1;
                        end
                    end
                else
                    q = element.q(2);
                    count = 1;
                    elementPoints = element.geElementPoints();
                    for n = 0:this.n
                        for m = -n:n
                            for o = 1:this.ng
                                for p = 1:this.ng
                                    xksi = zeros(3,1);
                                    shapeF = element.getShapeF(X(p,o),Y(p,o));
                                    for s = 1:length(elementPoints)
                                        xksi = xksi + shapeF(s)*...
                                            elementPoints(s).coord;
                                    end
                                    r = xksi - yc;
                                    jacobian = element.getJacobian(X(p,o),Y(p,o));
                                    element.MEG(count) = element.MEG(count) + ...
                                        (jacobian/0.5)*q*...
                                        this.RLook(r,this.indexNM(n,m),n)*Wx(p)*Wy(o)*shapeF(i);
                                end
                            end
                            count = count + 1;
                        end
                    end
                end
            end
        end
        function calculateMMT(this,element)
            element.MEH = zeros(this.totN,1);
            element.MEG = zeros(this.totN,1);
            children = element.children;
            ycL = element.getYc;
            for i = 1:length(children)
                elementChild = children(i);
                yc = elementChild.getYc;
                r = yc - ycL;
                R = this.R(2*this.n + 1,r,[]);
                count = 1;
                for n = 0:this.n
                    for m = -n:n
                        for nc = 0:this.n
                            for mc = -nc:nc
                                id = this.indexNM(n-nc,m-mc);
                                if (id > 0)
                                    element.MEG(count) = element.MEG(count) + ...
                                        R(this.indexNM(nc,mc))*...
                                        elementChild.MEG(id);
                                    element.MEH(count) = element.MEH(count) + ...
                                        R(this.indexNM(nc,mc))*...
                                        elementChild.MEH(id);
                                end
                            end
                        end
                        count = count + 1;
                    end
                end
            end
        end
        function calculateFarInt(this,cell)
            xl = cell.nodeCE;
            nodes = cell.nodes;
            for i = 1:length(nodes)
                node = nodes(i);
                x = node.pos;
                r = x - xl;
                R = this.R(this.n+1,r,[]);
                this.Ax(node.element.id) = this.Ax(node.element.id) + ...
                    (1/(4*pi))*R*cell.MLG;
                this.Ax(node.element.id) = this.Ax(node.element.id) + ...
                    (1/(4*pi))*R*cell.MLH;
            end
        end
